import Foundation

class Musica {
    
    var foto:String?
    var nome:String?
    var artista:String?
    var album:String?
    var tempo:String?
    
    init( foto:String, nome:String, artista:String, album:String,tempo:String){
        self.foto = foto
        self.nome = nome
        self.artista = artista
        self.album = album
        self.tempo = tempo
    }
    
}
